#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	num;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	num = (unsigned int) n;
	if (num >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(num % 10 + '0', fd);
	return ;
}
